<h3>Instructions</h3>
<hr>
<a class="swarm_effect_review_btn" href="https://wordpress.org/support/plugin/swarm-effect/reviews/">Reviews</a>
<p>Create a new notification by following these steps:</p>
<p>- Click "Add New" button</p>
<p>- Fill out all the fields on the first tab.</p>
<p>- Provide value for all the varibales you want to use in the notifications and go to next tab.</p>
<p>- Select the UI design for your notification and provide a time range (in seconds) for the notification to be displayed.</p>
<p>- Select the position on the screen where you want the notification to be displayed.</p>
<p>- Choose from the display rules and then save your notification.</p>

<p> Once everything is done you can activate/deactivate the notification from the list page.</p>
